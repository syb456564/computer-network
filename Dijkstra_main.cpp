//此程序可以输出指定路由器路由表，该文件为主函数
#include "Dijkstra_head.h"

//主函数
int main(void)
{
    int vernum,edge;
    Graph graph;
    cout << "请输入拓扑的路由器个数和边条数：" << endl;
    cin >> vernum >> edge;
    while (! graph.Check(vernum, edge))
	{
        cout << "输入不合法！请重新输入！" << endl;
        cin >> vernum >> edge;
    }
    graph.Create_graph(vernum, edge);
    system("pause");      //按任意键继续...

    first:
    switch(graph.Menu())
    {
    	case 1:
    	{
    		system("cls");//清除屏幕
			graph.Dijkstra();
			system("pause");
			goto first;
		}
		case 2:
    	{
    		system("cls");
    		graph.Del_edge();
			system("pause");
			goto first;
		}
		case 3:
    	{
    		system("cls");
    		graph.Add_edge();
			system("pause");
			goto first;
		}
		case 4:
    	{
    		system("cls");
    		graph.Del_vertex();
			system("pause");
			goto first;
		}
		case 5:
    	{
    		system("cls");
    		graph.Add_vertex();
			system("pause");
			goto first;
		}
	}
    return 0;
}
